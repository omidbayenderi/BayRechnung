// Helper function to detect user's browser language
export const detectUserLanguage = () => {
        // Get browser language (e.g., "en-US", "de-DE", "tr-TR")
        const browserLang = navigator.language || navigator.userLanguage;

        // Extract language code (e.g., "en", "de", "tr")
        const langCode = browserLang?.split('-')[0]?.toLowerCase();

        // Supported languages
        const supportedLangs = ['de', 'en', 'tr', 'fr', 'es'];

        // Return detected language if supported, otherwise default to English
        return supportedLangs.includes(langCode) ? langCode : 'en';
};

export const LanguageProvider = ({ children }) => {
        const [appLanguage, setAppLanguage] = useState(() => {
                // Dynamic detection: check browser first, then fallback to saved, then detect
                const browser = detectUserLanguage();
                const saved = localStorage.getItem('bay_app_language');

                // If we have a saved language, we use it, but detectUserLanguage provides the initial dynamic state
                return saved || browser;
        });

        const [invoiceLanguage, setInvoiceLanguage] = useState(() => {
                return localStorage.getItem('bay_invoice_lang') || appLanguage;
        });

        const [aiTranslations, setAiTranslations] = useState(() => {
                const stored = localStorage.getItem('bay_ai_translations');
                return stored ? JSON.parse(stored) : {};
        });

        // New: Manage languages for specific services/panels
        const [serviceLanguages, setServiceLanguages] = useState(() => {
                const saved = localStorage.getItem('bay_service_languages');
                // If saved exists, use it. If not, default ALL to current appLanguage (Smart Default)
                return saved ? JSON.parse(saved) : {
                        invoicing: appLanguage,
                        appointments: appLanguage,
                        website: appLanguage
                };
        });

        useEffect(() => {
                localStorage.setItem('bay_app_language', appLanguage);

                // SMART AGENT VALIDATION: Auto-Sync Panels
                // When Admin changes main language, we assume they want the whole admin panel (including Stock) to match.
                setServiceLanguages(prev => ({
                        ...prev,
                }));

                // Also update invoice print language if it wasn't manually set to something else recently? 
                // For now, let's keep invoiceLanguage separate as it's for "Output/Print".

        }, [appLanguage]);

        useEffect(() => {
                localStorage.setItem('bay_invoice_lang', invoiceLanguage);
        }, [invoiceLanguage]);

        useEffect(() => {
                localStorage.setItem('bay_service_languages', JSON.stringify(serviceLanguages));
        }, [serviceLanguages]);

        const setServiceLanguage = (serviceKey, languageCode) => {
                setServiceLanguages(prev => ({
                        ...prev,
                        [serviceKey]: languageCode
                }));
        };

        const reportedKeysRef = React.useRef(new Set());

        const t = (key, fallback) => {
                const langData = translations[appLanguage] || translations['en']; // Fallback to 'en' for base translations
                const aiData = aiTranslations[appLanguage] || {};
                const val = langData?.[key] || aiData?.[key];

                if (!val) {
                        // Continuous Monitoring: Log missing keys for the BayGlobe Agent
                        if (!reportedKeysRef.current.has(key)) {
                                reportedKeysRef.current.add(key);
                                if (typeof window !== 'undefined') {
                                        console.warn(`[BayGuard] Missing translation for key: "${key}" in language: "${appLanguage}"`); // Simple scanner log
                                        const event = new CustomEvent('bayglobe-missing-key', {
                                                detail: { key, lang: appLanguage }
                                        });
                                        // Fix: Dispatch asynchronously to avoid "setState in render" warning in React
                                        setTimeout(() => window.dispatchEvent(event), 0);
                                }
                        }
                        return fallback || key;
                }
                return val;
        };

        const updateAiTranslation = (lang, key, value) => {
                setAiTranslations(prev => {
                        const newState = {
                                ...prev,
                                [lang]: {
                                        ...(prev[lang] || {}),
                                        [key]: value
                                }
                        };
                        localStorage.setItem('bay_ai_translations', JSON.stringify(newState));
                        return newState;
                });
        };

        const tInvoice = (key) => {
                const langData = translations[invoiceLanguage] || translations['de'];
                const val = langData?.[key];

                if (!val) {
                        if (!reportedKeysRef.current.has(key)) {
                                reportedKeysRef.current.add(key);
                                if (typeof window !== 'undefined') {
                                        const event = new CustomEvent('bayguard-content-issue', {
                                                detail: { key, lang: invoiceLanguage }
                                        });
                                        window.dispatchEvent(event);
                                }
                        }
                        return key;
                }
                return val;
        };

        const getT = (langCode) => (key) => {
                const checkLang = langCode?.split('-')[0] || 'de';
                const validLang = translations[checkLang] ? checkLang : 'de';
                const val = translations[validLang]?.[key] || translations['en']?.[key] || translations['de']?.[key];
                return val || key;
        };

        const SUPPORTED_LANGUAGES = [
                { code: 'de', label: 'Deutsch', flag: '🇩🇪' },
                { code: 'en', label: 'English', flag: '🇺🇸' },
                { code: 'tr', label: 'Türkçe', flag: '🇹🇷' },
                { code: 'fr', label: 'Français', flag: '🇫🇷' },
                { code: 'es', label: 'Español', flag: '🇪🇸' }
        ];

        // Dynamically add languages from AI translations if they are not already in SUPPORTED_LANGUAGES
        const LANGUAGES = useMemo(() => {
                const dynamicLangs = Object.keys(aiTranslations)
                        .filter(langCode => !SUPPORTED_LANGUAGES.some(sl => sl.code === langCode))
                        .map(langCode => ({
                                code: langCode,
                                label: langCode.toUpperCase(), // Placeholder label, could be improved
                                flag: '🌐' // Generic flag
                        }));
                return [...SUPPORTED_LANGUAGES, ...dynamicLangs];
        }, [aiTranslations]);


        return (
                <LanguageContext.Provider value={{
                        appLanguage,
                        setAppLanguage,
                        invoiceLanguage,
                        setInvoiceLanguage,
                        t,
                        getT,
                        tInvoice,
                        serviceLanguages,
                        setServiceLanguage,
                        LANGUAGES,
                        updateAiTranslation,
                        aiTranslations,
                        translations
                }}>
                        {children}
                </LanguageContext.Provider>
        );
};

export const useLanguage = () => useContext(LanguageContext);
